/*
  Implementation of the GPRS encryption algorithms GEA1 and GEA2
  From the research paper:
  https://eprint.iacr.org/2021/819.pdf
  
  
  Copyright P1 Security, 2021/28/09
  
  This implementation has been developped for a 64 bits little-endian system
  complying with the C99 standard
*/
/*----------------------------------------------------------------------------*/

#include "gea12.h"
#include <stdio.h>


int main() {
    
    //int i, j, k;
    int i;
    //int i;
    
    // GEA inputs
    GEAInput gea1_in[3] = {
       {.iv  = {0x00, 0x00, 0x00, 0x00},
        .dir = 0,
        .key = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}},
       {.iv  = {0xda, 0x63, 0x7a, 0x83},
        .dir = 1,
        .key = {0x55, 0xe3, 0x03, 0xeb, 0x7d, 0x55, 0xb6, 0x85}},
       {.iv  = {0x0e, 0x9b, 0x8a, 0xdf},
        .dir = 0,
        .key = {0xa7, 0x26, 0x5d, 0x19, 0x32, 0xa0, 0xd6, 0x18}},
    };
    
    uint8_t GEA1PlainTexts[3][18] = {
        {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
        {0x6e, 0x00, 0xcf, 0xe7, 0xb7, 0xfb, 0x97, 0x48, 0x92, 0xb8, 0xcd, 0xe5, 0xe4, 0x33, 0x63, 0x39, 0x7d, 0x85},
        {0x96, 0xe7, 0xb1, 0xd9, 0x2b, 0x1e, 0xa8, 0xfc, 0xdd, 0xa4, 0x12, 0x33, 0xc6, 0x32, 0x94, 0x05, 0x53, 0x83}
    };
    
    uint8_t GEA2PlainTexts[3][18] = {
        {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
        {0xea, 0xbf, 0x6d, 0x3c, 0x6b, 0xa5, 0xdb, 0xf7, 0x6e, 0xbb, 0x3c, 0x4c, 0x0a, 0xc0, 0x24, 0x0c, 0xb0, 0xab},
        {0xf9, 0x37, 0x3d, 0xe5, 0x2e, 0xa6, 0x2c, 0x49, 0x06, 0x97, 0x11, 0xe8, 0x33, 0x89, 0xd0, 0x37, 0xfc, 0x17}
    };
    
    GEAInput gea2_in[3] = {
       {.iv  = {0x00, 0x00, 0x00, 0x00},
        .dir = 0,
        .key = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}},
       {.iv  = {0x24, 0xc0, 0x5b, 0x01},
        .dir = 1,
        .key = {0xb1, 0x0f, 0x38, 0x9b, 0x78, 0xa6, 0x16, 0x48}},
       {.iv  = {0xf5, 0x9c, 0xc9, 0x6a},
        .dir = 0,
        .key = {0x0c, 0x34, 0xb2, 0x94, 0x0a, 0x97, 0x07, 0xfd}},
    };
    
    // GEA output (to be filled-in)
    GEAOutput out;
    // all expected keystreams are 144 bits / 18 bytes
    out.len = 18;
    out.ks  = malloc(out.len);
    
    // corresponding expected output for GEA1
    uint8_t GEA1CipherTexts[3][18] = {
        {0x1f, 0xa1, 0x98, 0xab, 0x21, 0x14, 0xc3, 0x8a, 0x9e, 0xbc, 0xcb, 0x63, 0xad, 0x48, 0x13, 0xa7, 0x40, 0xc1},
        {0x58, 0xda, 0xd0, 0x64, 0x57, 0xb9, 0xfe, 0x10, 0x15, 0xda, 0x07, 0x76, 0xed, 0x19, 0x90, 0x7b, 0x78, 0x88},
        {0xd7, 0x21, 0x97, 0xf6, 0x5d, 0x4d, 0x67, 0xb1, 0x4d, 0x2c, 0xee, 0x81, 0x2c, 0xb0, 0xb9, 0xbe, 0xa0, 0xc9},
    };
    
    // corresponding expected output for GEA2
    uint8_t GEA2CipherTexts[3][18] = {
        {0x04, 0x51, 0x15, 0xD5, 0xE5, 0xA2, 0xD6, 0x25, 0x41, 0xDA, 0x07, 0x8B, 0x18, 0xBA, 0xA5, 0x3F, 0xFE, 0x14},
        {0x51, 0x56, 0x56, 0x9d, 0x2a, 0xb9, 0x82, 0x57, 0xbe, 0x1a, 0x37, 0xd6, 0x0d, 0xdf, 0x07, 0xae, 0x90, 0x75},
        {0x50, 0x9c, 0x19, 0xb7, 0x8d, 0x1d, 0x4c, 0xeb, 0x49, 0xc3, 0xb1, 0xf4, 0x3d, 0xf0, 0x14, 0xf7, 0x4c, 0xda},
    };
    
    
    for (i = 0; i < 3; i++) {
        
        memset(out.ks, 0, 18);
        gea1(&gea1_in[i], &out);
        
        /*
        printf("Keystream: 0x");
        for (j=0; j<out.len; j++) {
            printf("%.2x", out.ks[j]);
        };
        printf("\n");
        */
        
        for(int pos = 0; pos < 18; pos++) {
            out.ks[pos] ^= GEA1PlainTexts[i][pos];
        }
        
        if (memcmp(out.ks, GEA1CipherTexts[i], 18) != 0) {
            printf("GEA1 failed test %d\n", i);
        };
        
        memset(out.ks, 0, 18);
        gea2(&gea2_in[i], &out);
        
        /*
        printf("Keystream: 0x");
        for (j=0; j<out.len; j++) {
            printf("%.2x", out.ks[j]);
        };
        printf("\n");
        */
        
        for(int pos = 0; pos < 18; pos++) {
            out.ks[pos] ^= GEA2PlainTexts[i][pos];
        }
        
        if (memcmp(out.ks, GEA2CipherTexts[i], 18) != 0) {
            printf("GEA2 failed test %d\n", i);
        };
    };
    
    printf("all tests passed\n");
    return 0;
};

